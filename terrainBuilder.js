function _makeBasinSprite(name, tier) {
    const s = _TIER_STYLE[tier];
    const canvas = document.createElement('canvas');
    canvas.width  = s.canvasW;
    canvas.height = s.canvasH;
    const ctx = canvas.getContext('2d');

    ctx.textAlign    = 'center';
    ctx.textBaseline = 'middle';
    ctx.font         = `300 ${s.fontSize}px 'Courier New', monospace`;

    // Wide letter-spacing for the cartographic chart feel
    if ('letterSpacing' in ctx) ctx.letterSpacing = `${Math.round(s.fontSize * 0.55)}px`;

    // Two-pass glow — outer halo then sharp fill
    ctx.shadowColor = s.glow;
    ctx.shadowBlur  = s.blur;
    ctx.fillStyle   = s.fill;
    ctx.fillText(name, s.canvasW / 2, s.canvasH / 2);
    ctx.shadowBlur  = s.blur * 0.4;
    ctx.fillText(name, s.canvasW / 2, s.canvasH / 2);
    ctx.shadowBlur  = 0;

    const tex = new THREE.CanvasTexture(canvas);
    const mat = new THREE.SpriteMaterial({
        map: tex, transparent: true, depthTest: false, opacity: s.opacity,
    });
    const spr = new THREE.Sprite(mat);
    spr.scale.set(s.spriteW, s.spriteW * (s.canvasH / s.canvasW), 1);
    spr.renderOrder = 982;   // below port labels (998) and depth markers (990)
    return spr;
}

// Convert lon/lat → scene XZ using Mercator (matches lonLatToScene in aisManager)
function _lonLatToXZ(lon, lat) {
    const x      = (lon / 180.0) * (MAP_WIDTH  / 2.0);
    const latRad = lat * (Math.PI / 180.0);
    const mercY  = Math.log(Math.tan(Math.PI / 4.0 + latRad / 2.0));
    const z      = -(mercY / Math.PI) * (MAP_HEIGHT / 2.0);
    return { x, z };
}

export function createOceanBasinLabels(scene) {
    const group = new THREE.Group();
    group.name  = 'oceanBasinLabels';
    scene.add(group);

    // Y positions — labels float in the water column, clearly below the surface
    // but above the deep ocean floor so they're readable from oblique angles.
    const tierY = { 1: -1.2, 2: -0.9, 3: -0.6 };

    for (const feat of _BASIN_FEATURES) {
        const { x, z } = _lonLatToXZ(feat.lon, feat.lat);
        // Clamp near-polar labels to map bounds
        if (Math.abs(z) > MAP_HEIGHT * 0.48) continue;

        const sprite = _makeBasinSprite(feat.name, feat.tier);
        sprite.position.set(x, tierY[feat.tier], z);
        group.add(sprite);
    }

    return group;
}

// ── Point cloud crossfade LOD ─────────────────────────────────────────────────
// Call every frame alongside continentMesh.update(camera).
// Point cloud stays fully visible until close zoom, then crossfades with mesh.
//   camera.y > 25  → uFade = 1.0  (point cloud fully visible)
//   camera.y 25→10 → uFade 1→0    (fades as continent mesh fades in)
export function updatePointCloud(camera) {
    if (!_splatCloud) return;
    const fade = THREE.MathUtils.clamp(
        (camera.position.y - SPLAT_FADE_END) / (SPLAT_FADE_START - SPLAT_FADE_END), 0, 1
    );
    _splatCloud.material.uniforms.uFade.value = fade;
    _splatCloud.visible = fade > 0;

    // Compute tilt: dot of camera look direction with world up (0,1,0).
    // When looking straight down: dot ≈ -1 → tilt = 0 (no boost needed).
    // When looking horizontally:  dot ≈  0 → tilt = 1 (full boost applied).
    const lookDir = new THREE.Vector3();
    camera.getWorldDirection(lookDir);
    const tilt = THREE.MathUtils.clamp(1.0 + lookDir.y, 0, 1);
    _splatCloud.material.uniforms.uTilt.value = tilt;
}
