function _makeDepthSprite(name, depthM) {
    const W = 256, H = 72;
    const canvas = document.createElement('canvas');
    canvas.width  = W;
    canvas.height = H;
    const ctx = canvas.getContext('2d');

    // Background pill
    const rx = 6;
    ctx.beginPath();
    ctx.moveTo(rx, 2); ctx.lineTo(W - rx, 2);
    ctx.arcTo(W - 2, 2, W - 2, rx + 2, rx);
    ctx.lineTo(W - 2, H - rx); ctx.arcTo(W - 2, H - 2, W - rx - 2, H - 2, rx);
    ctx.lineTo(rx + 2, H - 2); ctx.arcTo(2, H - 2, 2, H - rx - 2, rx);
    ctx.lineTo(2, rx + 2);     ctx.arcTo(2, 2, rx + 2, 2, rx);
    ctx.closePath();

    const isTrench = depthM > 3000;
    ctx.fillStyle   = isTrench ? 'rgba(0, 4, 14, 0.90)' : 'rgba(0, 8, 20, 0.82)';
    ctx.fill();
    ctx.strokeStyle = isTrench ? 'rgba(64, 196, 255, 0.75)' : 'rgba(0, 200, 160, 0.60)';
    ctx.lineWidth   = 1.5;
    ctx.stroke();

    // Feature name (small, dimmer)
    ctx.textAlign    = 'center';
    ctx.textBaseline = 'alphabetic';
    ctx.font         = 'bold 10px Courier New';
    ctx.fillStyle    = isTrench ? 'rgba(64, 196, 255, 0.70)' : 'rgba(0, 220, 170, 0.70)';
    ctx.fillText(name, W / 2, 24);

    // Depth reading (larger, bright)
    const depthStr = `▼ ${depthM.toLocaleString()} m`;
    ctx.font        = 'bold 18px Courier New';
    ctx.shadowColor = isTrench ? '#40c4ff' : '#00dca0';
    ctx.shadowBlur  = 8;
    ctx.fillStyle   = isTrench ? 'rgba(64, 196, 255, 0.98)' : 'rgba(0, 220, 160, 0.95)';
    ctx.fillText(depthStr, W / 2, 56);
    ctx.shadowBlur  = 0;

    const tex = new THREE.CanvasTexture(canvas);
    const mat = new THREE.SpriteMaterial({ map: tex, transparent: true, depthTest: false, opacity: 0.92 });
    const spr = new THREE.Sprite(mat);
    spr.scale.set(14, 14 * (H / W), 1);
    spr.renderOrder = 990;
    return spr;
}

export function createOceanDepthMarkers(scene) {
    const group = new THREE.Group();
    group.name  = 'oceanDepthMarkers';
    scene.add(group);

    for (const feat of OCEAN_FEATURES) {
        // Scene XZ via Mercator (consistent with lonLatToScene / ship positions)
        const x      = (feat.lon / 180.0) * (MAP_WIDTH / 2.0);
        const latRad = feat.lat * (Math.PI / 180.0);
        const mercY  = Math.log(Math.tan(Math.PI / 4.0 + latRad / 2.0));
        const z      = -(mercY / Math.PI) * (MAP_HEIGHT / 2.0);

        // Ocean floor Y — mirrors createSolidOceanFloor's displacement formula
        const hMeters = getBestElevation(x, z);
        const dist    = Math.sqrt((x / MAP_WIDTH) ** 2 + (z / MAP_HEIGHT) ** 2);
        const curveY  = -Math.pow(dist, 2) * 20.0;
        const floorY  = (hMeters / 800.0) * TERRAIN_VSCALE_OCEAN + curveY - 0.2;

        // Label floats above the floor; stem connects label to floor
        const stemHeight = Math.max(1.5, Math.abs(hMeters) / 800.0 * 0.18);
        const labelY     = floorY + stemHeight + 1.5;

        // Stem line
        const stemGeo = new THREE.BufferGeometry().setFromPoints([
            new THREE.Vector3(x, floorY + 0.1, z),
            new THREE.Vector3(x, labelY - 0.8,  z),
        ]);
        const stemMat = new THREE.LineBasicMaterial({
            color: feat.depthM > 3000 ? 0x40c4ff : 0x00dca0,
            transparent: true, opacity: 0.45, depthTest: false,
        });
        group.add(new THREE.Line(stemGeo, stemMat));

        // Label sprite
        const sprite = _makeDepthSprite(feat.name, feat.depthM);
        sprite.position.set(x, labelY, z);
        group.add(sprite);
    }

    return group;
}

